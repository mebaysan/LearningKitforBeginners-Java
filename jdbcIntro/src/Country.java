



public class Country {
  private String code; // özellikleri belirledik
  private String name;
  private String continent;
  private String region ;
  
  public Country(String code,String name, String continent, String region){
      this.code = code; // burdan bir nesne oluşurken parametre olarak verilen değerleri nesnenin özelliklerine ata dedik.
      this.name=name;
      this.continent=continent;
      this.region=region;
      
  }
  
  
  
  
}
