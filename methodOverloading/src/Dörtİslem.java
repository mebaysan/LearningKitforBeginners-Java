
public class Dörtİslem {
    
    public int Topla(int sayi1,int sayi2){
        return sayi1+sayi2;
        
        // burda method overloading yaptık.
        // aynı methoda ekleme yaptık ve aşırı yükleme denilen olayı yaptık. 
        
        //ister 2 parametre ile ister 3 parametre ile çalışır hale geldi.
    }
    public int Topla(int sayi1,int sayi2, int sayi3){
        return sayi1+sayi2+sayi3;
    }


}
